                    <h1 class="blog-post-title">Educação:Curitiba</h1>
                    <p class="blog-post-meta">04/03/2018 <a href="http://www.gazetadopovo.com.br/opiniao/artigos/um-triste-recuo-para-a-educacao-infantil-em-curitiba-2x9jyjwswxr335i3q5rhpw41f" target="_blank">Gazeta do Povo</a></p>
                    <p>O Conselho Municipal de Educação de Curitiba aprovou, em 22 de fevereiro, uma deliberação que anuncia um enorme retrocesso para a educação infantil pública na capital. O texto aprovado flexibiliza a regra de ingresso de profissionais para atuarem na educação infantil dos Centros Municipais de Educação Infantil (CMEIs) da rede pública. Na prática, significa que poderão atuar nos CMEIs pessoas sem ao menos a formação mínima hoje exigida, o ensino médio Normal (conhecido como “Magistério”).</p>
                    
                    <hr>
                    
                    <p>O direito à educação infantil como parte da educação básica é um avanço da Lei de Diretrizes e Bases da Educação Nacional aprovada em 1996 – direito conquistado, portanto, já há 22 anos. Mas ainda temos muitas crianças de zero a 3 anos (na idade da etapa da creche) e de 4 e 5 anos (na idade da etapa da pré-escola) que não alcançaram este direito.</p>
                    <hr>
                    <p>O Plano Nacional de Educação (PNE) de 2014 estabelece que o país deveria, até 2016, matricular na pré-escola 100% das crianças de 4 e 5 anos. Em 2016, Curitiba alcançou a meta de universalizar o atendimento da pré-escola. De acordo com dados do Inep divulgados no mesmo período, a capital paranaense tornou-se também a primeira colocada entre as capitais no atendimento de crianças de zero a 5 anos em tempo integral, atingindo a marca de 86%; o segundo lugar, Rio de Janeiro, atendeu 44% das crianças nesta faixa etária. No caso das creches, a meta nacional é de 50% do atendimento até 2024; dados disponíveis no <a href="http://simec.mec.gov.br/pde/graficopne.php" target="_blank">sistema do MEC</a> informam que em Curitiba o atendimento é de 44%.</p>

                    <hr>

                    <blockquote>
                        Estamos falando da primeira etapa da educação básica, que exige uma intencionalidade pedagógica
                    </blockquote>

                    <hr>
                    <p>O problema da matrícula é, certamente, o primeiro desafio para o direito à educação. Mas é fundamental pensar que educação queremos para as crianças. O mesmo PNE, que precisa ser lido com suas metas articuladas, estabelece a necessidade de que todos os profissionais da educação tenham licenciatura para trabalhar na educação básica. </p>

                    <hr>

                    <p>O Plano de Carreira de Professores da Educação Infantil em Curitiba, implantado em 2015 (Lei 14.580/2014), apresentou para aproximadamente 4,4 mil profissionais uma nova projeção da carreira. Entre os principais benefícios destaca-se a transformação da carreira de “educador” para “professor de educação infantil”, em consonância com a LDB, como também a possibilidade de alcançar o topo da carreira com 25 anos de serviço e aumentos salariais diferenciados, estimulando a formação inicial e continuada dos profissionais. Para tanto, reforçou-se neste plano (no artigo 2.º, § 1) a necessidade de que os profissionais que atuam na docência nas turmas de educação infantil da cidade tenham a formação mínima estabelecida na LDB – ensino médio, modalidade normal (magistério) – e avancem na formação com licenciatura em Pedagogia. </p>
                    <hr>

                    <p>Desde 2014, em parceria com o Setor de Educação da Universidade Federal do Paraná, a prefeitura de Curitiba, por meio da Secretaria Municipal de Educação, trabalhou pela busca da garantia de cursos de Pedagogia para os profissionais que ainda não tinham esta possibilidade e atuam na educação infantil da rede municipal. A concretização desta oportunidade se deu em 2016, e perdura até o momento. Foram abertas vagas no curso de Pedagogia (semipresencial/modalidade a distância) institucionalizado pela UFPR em convênio com a prefeitura, como também em curso ofertado pelo Polo UAB/Capes, garantindo o direito de formação para professores e professoras da educação infantil de Curitiba. Nada mais justo, uma vez que estes profissionais precisam ser valorizados. </p>

                    <hr>

                    <p>Precisamos lembrar que estamos falando da primeira etapa da educação básica, desenvolvida em um espaço formal, que exige uma intencionalidade pedagógica, que implica planejar e mediar experiências que ampliem os repertórios culturais e de conhecimentos das crianças, o que exige conhecimentos que só aqueles que passam por um processo de formação específico para atuar na docência têm.Tratar as crianças como sujeitos de direitos, cidadãos da capital, exige que tais parâmetros de atendimento sejam garantidos. </p>

                    <hr>
                    
                    <p>Todos os profissionais que atuam no âmbito de uma sala de aula atuam na docência. Em qualquer relação pedagógica, sempre o docente cuida e educa. São elementos indissociáveis – e quanto menor a criança, mais esta relação indissociável se faz necessária. O que garante esta possibilidade é a formação adequada do adulto que entra no espaço educativo. Flexibilizar a regra de ingresso de profissionais para atuarem na educação infantil é retroceder no respeito aos cidadãos e cidadãs curitibanos que têm menos de 5 anos. Não faz sentido uma cidade que tem uma história de atendimento de qualidade na sua rede de ensino economizar na vida das crianças. É preciso que o Conselho Municipal de Educação reveja a posição, é urgente e necessário que a Secretaria Municipal de Educação de Curitiba reconsidere o apoio a esta decisão e que se afirme o compromisso com a qualidade da educação municipal. </p>
