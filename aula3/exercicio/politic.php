                    <p>Os regimes parlamentaristas estão, de certa maneira, mais protegidos dos extremistas de todos as fidelidades políticas. O sistema torna praticamente impossível a construção de unanimidade. No parlamentarismo, a capacidade de negociação não é apenas necessária, mas prerrogativa a todos os partidos. Em função disso, a Holanda deve se manter a salvo, mais uma vez, de Geert Wilders.</p>
