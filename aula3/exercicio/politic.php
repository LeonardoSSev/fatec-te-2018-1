                    <h2>Perspectivas europeias</h2>
                    <hr>
                    <p>As eleições holandesas marcam o início do ciclo de decisões europeias em atores mais relevantes. A Holanda, um dos países fundadores da UE, vai às urnas antecipando ansiedades diante da possibilidade de reviravolta nos cenários políticos francês (segundo turno em maio com a provável participação de Marine Le Pen) e alemão (eleições em setembro que podem encerrar 11 anos de mandato de Angela Markel, cada vez mais representativa da resistência do liberalismo e integração europeias). Conjuntamente, este ano de decisões passou a ostentar contornos mais dramáticos num ambiente de crise de identidade e, novamente, fortalecimento da extrema-direita. </p>
